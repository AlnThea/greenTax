                <li>Utilize car-sharing services rather than owning vehicles</li>
                <li>Embrace micro-mobility options like electric scooters and bicycles</li>
                <li>Consider proximity to public transit when making housing decisions</li>
            </ul>
            
            <p>In urban areas, congestion charges and road pricing schemes further influence transportation behavior. London's Congestion Charge, for instance, requires drivers to pay a daily fee to enter central London during business hours, encouraging the use of public transportation and reducing traffic and emissions.</p>
            
