            <p>Green taxes have tangible effects on Generation Z's daily lives, influencing everything from transportation choices to consumption patterns. Understanding these impacts is essential for navigating an increasingly eco-conscious economy.</p>
            
            <h3 class="text-2xl font-bold text-emerald-600 mb-4 mt-8">Transportation Costs and Choices</h3>
            
            <p>For Gen Z, transportation-related green taxes will significantly shape mobility options and costs. As governments worldwide implement or increase taxes on fossil fuels, conventional vehicles become more expensive to operate. According to the European Commission's green taxation framework, "Energy taxes are taxes on energy products and electricity used for transport, such as petrol and diesel, and for other purposes."</p>
            
            <p>These taxes create financial incentives for Gen Z to:</p>
            
            <ul class="list-disc pl-6 mb-6">
